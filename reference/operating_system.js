const os = require('os');

//Platform
console.log(os.platform());

//Central Processing Architecture
console.log(os.arch());

//Central Processing Unit Core Information
console.log(os.cpus());

// Free Memory
console.log(os.freemem());

// Total Memory
console.log(os.totalmem());

// Home directory
console.log(os.homedir());

// Uptime
console.log(os.uptime());
